package Basic

import "fmt"

func Variable() {
	fmt.Println("Variable Learning...")

	var one, two, three int

	var (
		name       string
		age        int
		profession string
	)

	one = 1
	two = 2
	three = 3

	fmt.Println(one)
	fmt.Println(two)
	fmt.Println(three)

	name = "minhaj"
	age = 24
	profession = "programmer"

	fmt.Println(name)
	fmt.Println(age)
	fmt.Println(profession)

}
